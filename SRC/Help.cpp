//--------------------------------------------------------------------------//
//	●	初期化	：シーンに移る時一回、変数の初期化
//--------------------------------------------------------------------------//
void	HelpInit(void)
{

}

//--------------------------------------------------------------------------//
//	●	内部処理：シーン中の処理、現在のシーンで行う処理
//--------------------------------------------------------------------------//
void	HelpLoop(void)
{

	if (Gs_GetKEYtrg(kKEYENTER)) {
		SceneChange(TITLE_SCENE);
	}
}

//--------------------------------------------------------------------------//
//	●	描画	：シーン中の描画、現在のシーンで表示するBMPはここで
//--------------------------------------------------------------------------//
void	HelpDraw(void)
{

	Gs_DrawText(100,300,"ヘルプシーン",ARGB(255,100,200,200));

}

//--------------------------------------------------------------------------//
//	●	ロード	：シーンに入る時、そのシーンで必要なデータの読み込み
//--------------------------------------------------------------------------//
short	HelpLoad(void)
{
	return		HELP_SCENE;
}

//--------------------------------------------------------------------------//
//	●	後処理	：現在のシーンから抜ける時、データの破棄
//--------------------------------------------------------------------------//
void	HelpExit(void)
{
}

//--------------------------------------------------------------------------//
//	★　シーンテーブル：各シーンで呼び出される関数の登録
//--------------------------------------------------------------------------//

SCENE_TBL	HelpSceneTbl = {
		HelpLoad,				// シーンが切り替わった時、各シーンで使用する絵、音のロード
		HelpInit,				// 各シーンで使用する変数初期化
		HelpLoop,				// 各シーンのメイン処理
		HelpDraw,				// 各シーンの描画
		HelpExit					// シーンから抜ける（移動する）時の各シーンの後処理(ＢＭＰなどの破棄）
};

